        private void update_update_details()
        {
            SqlConnection conn = new SqlConnection(My.conn);
            SqlDataAdapter ad = new SqlDataAdapter("select * from PRL_Department_Master where Id='" + hd_id.Value + "'", conn);
            DataSet ds = new DataSet();
            ad.Fill(ds);
            DataTable dt = ds.Tables[0];
            foreach (DataRow dr in dt.Rows)
            {
                dr[1] = txt_name.Text;
                dr[2] = txt_description.Text;
            }
            SqlCommandBuilder cb = new SqlCommandBuilder(ad);
            ad.Update(dt);
            Alertme("Department Master Updated Successfully", "success");
        }

        private void submit_details()
        {
            SqlConnection conn = new SqlConnection(My.conn);
            SqlDataAdapter ad = new SqlDataAdapter("select * from PRL_Department_Master", conn);
            DataSet ds = new DataSet();
            ad.Fill(ds);
            DataTable dt = ds.Tables[0];
            DataRow dr = dt.NewRow();
            dr[1] = txt_name.Text;
            dr[2] = txt_description.Text;
            dr[3] = My.auto_serialS("department_id");
            dt.Rows.Add(dr);
            SqlCommandBuilder cb = new SqlCommandBuilder(ad);
            ad.Update(dt);
            Alertme("Department Master Created Successfully", "success");
        }

        private void empty_form()
        {
            txt_name.Text = "";
            txt_description.Text = "";
            btn_cancel.Visible = false;
            btn_Submit.Text = "Add";
        }


        protected void lnkEdit_Click(object sender, EventArgs e)
        {
            try
            {
                if (ViewState["Is_Edit"].ToString() == "1")
                {
                    LinkButton lnk = (LinkButton)sender;
                    RepeaterItem row = (RepeaterItem)lnk.NamingContainer;
                    Label lbl_name = (Label)row.FindControl("lbl_name");
                    Label lbl_description = (Label)row.FindControl("lbl_description");

                    Label lbl_Id = (Label)row.FindControl("lbl_Id");
                    hd_id.Value = lbl_Id.Text;

                    txt_name.Text = lbl_name.Text;
                    txt_description.Text = lbl_description.Text;

                    btn_cancel.Visible = true;
                    btn_Submit.Text = "Update";
                }
                else
                {
                    Alertme(My.get_restricted_message(), "warning");
                }
            }
            catch
            {
            }
        }

        protected void lnkDel_Click(object sender, EventArgs e)
        {
            if (ViewState["Is_delete"].ToString() == "1")
            {
                LinkButton lnk = (LinkButton)sender;
                RepeaterItem row = (RepeaterItem)lnk.NamingContainer;
                Label lbl_Id = (Label)row.FindControl("lbl_Id");
                Label lbl_department_id = (Label)row.FindControl("lbl_department_id");
                if (is_true(lbl_department_id.Text))
                {
                    My.exeSql("delete from PRL_Department_Master where Id='" + lbl_Id.Text + "'");
                    Alertme("Department has been deleted successfully", "success");
                    bind_grd_view();
                }
                else
                {
                    Alertme("Department can't be deleted because it is used in Employee Master.", "warning");
                }
            }
            else
            {
                Alertme(My.get_restricted_message(), "warning");
            }
        }



        private bool is_true(string depid)
        {
            if (My.dataTable("select Department_id from dbo.[PRL_Employee_Master]  where Department_id='" + depid + "' ").Rows.Count > 0)
            {
                return false;
            }
            else
            {
                return true;
            }
        }

